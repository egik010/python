# Реализовать функцию int_func(), принимающую слово
# из маленьких латинских букв и возвращающую его же,
# но с прописной первой буквой. Например, print(int_func(‘text’)) -> Text.
# Продолжить работу над заданием.
# В программу должна попадать строка из слов,
# разделенных пробелом. Каждое слово состоит из
# латинских букв в нижнем регистре. Сделать вывод исходной строки,
# но каждое слово должно начинаться с заглавной буквы.
# Необходимо использовать написанную ранее функцию int_func().

def int_func(my_str):
    # return (my_str.capitalize())  **халявный вариант**
    return my_str[0].upper() + my_str[1:]


print(int_func('text'))
# вторая часть задачи
print('Введите строки через пробел')
my_list = list(map(str, input().split()))
print([(int_func(i)) for i in my_list])
